// Verifies our JS Poseidon over BLS12-381 matches the on-chain circuit's
// Poseidon (values pulled from the real proof in circuits/build/claim_input.json).
// Run: node --import tsx/esm --test src/poseidon.test.ts
import { describe, it } from "node:test";
import assert from "node:assert/strict";
import { poseidon } from "./poseidon.js";

describe("poseidon over BLS12-381 vs the on-chain circuit", () => {
  it("Poseidon([secret=12345]) matches the nullifier in claim_input.json", () => {
    // circuits/build/claim_input.json → "nullifier"
    const expected =
      "30210038655452570927876935731677491109181541264348558594944850742778338046562";
    assert.equal(poseidon(["12345"]), expected);
  });

  it("Poseidon([0, 0]) matches zeroHashes[1] (pathElements[1])", () => {
    // circuits/build/claim_input.json → pathElements[1]
    const expected =
      "48926953132787607730253737140721681335977819380115712260930162847120944266363";
    assert.equal(poseidon(["0", "0"]), expected);
  });

  it("Poseidon(zeroHashes[1], zeroHashes[1]) matches zeroHashes[2]", () => {
    const z1 = poseidon(["0", "0"]);
    const expected =
      "49012805093604247771405659364228463147692645079936381021870068641713100155282";
    assert.equal(poseidon([z1, z1]), expected);
  });

  it("full 20-level chain of Poseidon(z,z) matches pathElements[1..19]", () => {
    // pathElements is the array of 20 zero-subtree hashes: [0, P(0,0), P(P(0,0),P(0,0)), ...]
    // Verify all 19 chained values match the circuit output.
    const expected = [
      "0",
      "48926953132787607730253737140721681335977819380115712260930162847120944266363",
      "49012805093604247771405659364228463147692645079936381021870068641713100155282",
      "16151743990255126921066669447617739820848724188945092149080184096520996827612",
      "7189766564732299090829354577461536759512762386419123052045701740905422970126",
      "47411116528603606800938660217359893950440409719972312321217389314111789387955",
      "34137657464342980535813763843164701671321303682664116064965074721596765487370",
      "6497858151169723713281408859595075606273166203258421189377981906578975665807",
      "11713768589763190323357608453050673575482440637501859329578908328525176015862",
      "11404114723604512782553861665083887595999114476911729333375671418490835291583",
      "34836371484758956864355794477689131088297580299861030723270185871664588020331",
      "8498997386930922576691934612503185804507651123841045607978663179350941841374",
      "37791562111793584797332689222577133008320961677154850302097273766801912544274",
      "14735181231566311360656268391107164669431000453534256127422174900511124988367",
      "10075435513469036035134919902411844247117026759908211343986948165318271019114",
      "17246088542780932864211761553353438465170503598415768465007622481507515857132",
      "23762530828187563556471351702820423117782799100250171479413073733757311065392",
      "840337265531115767938356234666721276093968215947416647464981953109139758597",
      "41378598137789079356784732189582661540288702298764768659442407872299963945105",
      "39219585666303231345014895230299226691587148777097531876872086151202029309193",
    ];
    let z: string = "0";
    for (let i = 1; i < 20; i++) {
      z = poseidon([z, z]);
      assert.equal(z, expected[i], `zeroHashes[${i}] mismatch`);
    }
  });

  it("full deposit → root: leaf Poseidon(12345,42,10) up 20 zero levels == root", () => {
    // Reproduce the on-chain test proof end-to-end using ONLY our JS Poseidon.
    const commitment = poseidon(["12345", "42", "10"]);
    let node: string = commitment;
    let z: string = "0";
    for (let i = 0; i < 20; i++) {
      node = poseidon([node, z]);
      z = poseidon([z, z]);
    }
    const expectedRoot =
      "19148948013232879213203992136026734822699351263916619827234416357547906460635";
    assert.equal(node, expectedRoot);
  });
});
