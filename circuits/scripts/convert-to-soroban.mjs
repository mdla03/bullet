// Convert snarkjs BLS12-381 vk/proof/public JSON into Soroban bls12_381 byte
// layout (hex), for the soroban-contract verifier + its correctness test.
//   G1 (96B): BE(X) || BE(Y)
//   G2 (192B): BE(X_c1) || BE(X_c0) || BE(Y_c1) || BE(Y_c0)   <- c1 BEFORE c0
//   Fr (32B): BE
// snarkjs stores Fp2 as [c0, c1], so we swap to c1,c0.
import fs from "fs";

// claim circuit build uses claim_vk/claim_proof/claim_public;
// fall back to the benchmark names for backward compatibility.
const dir = "circuits/build";
const vkFile = fs.existsSync(`${dir}/claim_vk.json`) ? "claim_vk" : "verification_key";
const proofFile = fs.existsSync(`${dir}/claim_proof.json`) ? "claim_proof" : "proof";
const pubFile = fs.existsSync(`${dir}/claim_public.json`) ? "claim_public" : "public";
const vk = JSON.parse(fs.readFileSync(`${dir}/${vkFile}.json`));
const proof = JSON.parse(fs.readFileSync(`${dir}/${proofFile}.json`));
const pub = JSON.parse(fs.readFileSync(`${dir}/${pubFile}.json`));

const be = (dec, bytes) => {
  const h = BigInt(dec).toString(16);
  if (h.length > bytes * 2) throw new Error(`overflow: ${dec}`);
  return h.padStart(bytes * 2, "0");
};
const g1 = (p) => be(p[0], 48) + be(p[1], 48);
const g2 = (p) => be(p[0][1], 48) + be(p[0][0], 48) + be(p[1][1], 48) + be(p[1][0], 48);
const fr = (d) => be(d, 32);

const out = {
  alpha1: g1(vk.vk_alpha_1),
  beta2: g2(vk.vk_beta_2),
  gamma2: g2(vk.vk_gamma_2),
  delta2: g2(vk.vk_delta_2),
  ic: vk.IC.map(g1),
  a: g1(proof.pi_a),
  b: g2(proof.pi_b),
  c: g1(proof.pi_c),
  pubs: pub.map(fr),
};
fs.writeFileSync(`${dir}/groth16_soroban.json`, JSON.stringify(out, null, 2));

// Also emit a Rust fixture (tracked) for the verifier correctness test.
const icArr = out.ic.map((h) => `    "${h}",`).join("\n");
const pubArr = out.pubs.map((h) => `    "${h}",`).join("\n");
const rs = `// @generated by circuits/scripts/convert-to-soroban.mjs — DO NOT EDIT.
// Real Groth16/BLS12-381 proof from the local benchmark setup, encoded in
// Soroban bls12_381 byte layout. Public, throwaway-setup data (safe to commit).
pub const ALPHA1: &str = "${out.alpha1}";
pub const BETA2: &str = "${out.beta2}";
pub const GAMMA2: &str = "${out.gamma2}";
pub const DELTA2: &str = "${out.delta2}";
pub const IC: &[&str] = &[
${icArr}
];
pub const PROOF_A: &str = "${out.a}";
pub const PROOF_B: &str = "${out.b}";
pub const PROOF_C: &str = "${out.c}";
pub const PUBS: &[&str] = &[
${pubArr}
];
`;
fs.writeFileSync("contracts/zeekpay/src/groth16_fixture.rs", rs);
console.log("wrote groth16_soroban.json + contracts/zeekpay/src/groth16_fixture.rs");
console.log("ic:", out.ic.length, "pubs:", out.pubs.length, "| G1 hex:", out.alpha1.length, "G2:", out.beta2.length, "Fr:", out.pubs[0].length);
